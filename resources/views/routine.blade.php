            <h3>LEARNING THROUGH PLAY</h3>
            <p>
                 Play helps young children to learn and develop through doing and talking, which research has shown to be the means by which young children learn to think. Our setting uses the practice guidance Early Years Foundation Stage to plan and provide a range of play activities which help children to make progress in each of the areas of learning and development. In many of these activities children decide how they will use the activity and, in others, an adult takes the lead in helping the children to take part in the activity. In all activities information from the practice guidance to the Early Years Foundation Stage has been used to decide what equipment to provide and how to provide it
            </p>
            <h3>HOW WE PROVIDE FOR DEVELOPMENT AND LEARNING</h3>
            <p>
                Children start to learn about the world around them from the moment they are born. The care and education offered by our setting helps children to continue to do this by providing all of the children with interesting activities that are appropriate for their age and stage of development.
                The Prime Areas of Learning and Development are: * Personal, social and emotional development * Communication and language * Physical development.
                The four specific areas of development are:
                    <ul>
                        <li>Literacy</li>
                        <li>Mathematics</li>
                        <li>Understanding the world</li>
                        <li>Expressive arts and design.</li>
                    </ul> 
            </p>
            <p>
                    For each area, the practice guidance sets out the Early Learning Goals. These goals state what it is expected that children will know and be able to do by the end of the reception year of their education.
                    The practice guidance also sets out the likely stages of progress a child makes along their learning journey towards the early learning goals. Our setting has regard to these matters when we assess children and plan for their learning.
                    Prime Areas Personal, social and emotional development <br>
                   <em>Our programme supports children to develop:</em> 
            </p>
            <p>
                    <b>Self- confidence and self- awareness</b> — children are confident to try new activities, and say why they like some activities more than others. They are confident to speak in a familiar group, will talk about their ideas, and will choose the resources they need for their chosen activities. They say when they do or do not need help. <br>
                    <b>Managing feelings and behaviour</b>  — children talk about how they and others show feelings, talk about their own and other’s behaviour, and its consequences, and know that some behaviour is unacceptable. They work as part of a group or class, and understand and follow the rules. They adjust their behaviour to different situations, and take changes of routine ist their stride. <br>
                    <b>Making relationships</b>  — children play co-operatively, taking turns with others. They take account of one another's ideas about how to organise their activity. They show sensitivity to other’s needs and feeling, and form positive relationships with adults and other children.
                </p>  
                <p>      
                   <b>Communication and language</b> <br>
                    <em>Our programme supports children to develop:</em>  <br>
                    <b>Listening and attention</b> — children listen attentively in a range of situations. They listen to stories, accurately anticipating key events and respond to what they hear with relevant comments, questions or actions. They give
                    their attention to what others say and respond appropriately, while engaged in another activity. <br>
                    <b>Understanding</b> — children follow instructions involving several ideas or actions. They answer “how” and “why” questions about their experiences and in response to stories or events.
                    <br>
                    <b>Speaking</b>  — children express themselves effectively, showing awareness of listener’s needs. They use past, present and future forms accurately when talking about events that have happened or are to happen in the future. They develop their own narrative and explanations by connecting ideas or events.<br>
                    <br>
                    <b>Physical development</b>  <br> 
                     Our programme supports children to develop: <br>
                    <b>Moving and handling</b> — children show good control and co-ordination in large and small movements. They move confidently in a range of ways, safely negotiating space. They handle equipment and tools effectively, including pencils for writing. <br>
                    <b>Health and self — care</b>  — children know the importance for good health of physical exercise, and a healthy diet, and talk about ways to keep healthy and safe. They manage their own basic hygiene and personal needs successfully, including dressing and going to the toilet independently.<br>
                     <br>
                    <b>Literacy</b><br>
                     Our programme supports children to develop: <br>
                    <b>Reading</b> - children read and understand simple sentences. They use phonic knowledge to decode regular words and read them aloud accurately. They also read some common irregular words. They demonstrate understanding when talking with others about what they have read. <br>
                    <b>Writing</b> - children use their phonic knowledge to write words in ways which match their spoken sounds. They write simple sentences which can be read by themselves and others. Some words are spelt correctly and others are phonetically plausible. <br>
                    <br>
                    <b>Mathematics</b><br>
                     Our programme supports children to develop:  <br>
                    <b>Numbers</b> - children count reliably with numbers from 1 to 20, place them in order and say which number is one more or one less than a given number. Using quantities and objects, they add and subtract two single - digit numbers and count on or back to find the answer. <br>
                    <b>Shape, space and measures</b> - children use everyday language to talk about size, weight, Capacity, position, distance, time and money to compare quantities and objects and to solve problems. They recognise, create and describe patterns. They explore characteristics of everyday objects and shapes and use mathematical language to describe them. <br>
                    <br>
                    <b>Understanding-the world  </b> <br>
                    Our programme supports children to develop:<br>
                    <b>People and: communities</b> - children talk about past and present events in their own lives and in the lives of family members. They know that other children don’t always enjoy the same things, and are sensitive to this. They know about similarities and differences between themselves and others, and among families, communities and traditions. <br>
                    <b>The World</b> - children know about similarities and differences in relation to places, objects, materials and living things. They talk about the features of their own immediate environment and how environments might vary from one another. They make observations of animals and plants to explain why some things occur, and talk about changes. <br>
                    <b>Technology</b> - children recognise that a range of technology is used in places such as homes and schools. They select and use technology for particular purposes. <br>
                    <br>
                    <b>Expressive arts and design</b><br>
                    Our programme supports children to develop: <br>
                    <b>Exploring and using media and materials</b> - children sing songs, make music and dance, and experiment with ways of changing them. They safely use and explore a variety of materials, tools and techniques, experimenting with colour, texture, form and function. <br>
                    <b>Being imaginative</b> - children use what they have learnt about media and materials in original ways, thinking about uses and purposes. They represent their own ideas, thoughts and feelings through design and technology, art, music, role — play and stories. <br>
